def write_report(rows: list[dict[str, Any]]) -> None:
    ready = [r for r in rows if r["scan_decision"] == "ready_for_source_result_build"]
    needs = [r for r in rows if r["scan_decision"] != "ready_for_source_result_build"]
    report = [
        "# Figure 1 Individual Replication Batch 001 Value Scan",
        "",
        "This scan records what the mirrored source objects support. It does not edit root Figure 1 tables.",
        "",
        "## Summary",
        "",
        f"- Ready for source-result build now: {len(ready)} result options.",
        f"- Needs policy or exact-N reconciliation first: {len(needs)} result options.",
        "- This scan now covers the initially mirrored non-root individual candidates plus the open-text batch-002 candidates with value evidence or clear blockers.",
        "",
        "## Ready Now",
        "",
    ]
    for row in ready:
        report.append(
            f"- `{row['candidate_id']}` / {row['result_option_label']}: "
            f"original {row['original_effect_value']} (N={row['original_n_for_effect']}); "
            f"replication {row['replication_effect_value']} (N={row['replication_n_for_effect']})."
        )
    report.extend(["", "## Needs Decision Or Reconciliation", ""])
    for row in needs:
        report.append(
            f"- `{row['candidate_id']}` / {row['result_option_label']}: {row['scan_decision']}. "
            f"Next: {row['next_action']}."
        )
    report.extend(
        [
            "",
            "## Artifacts",
            "",
            f"- Value scan TSV: `{OUT_TSV.relative_to(ROOT)}`",
            "- Mirror status TSVs: "
            + ", ".join(f"`{path.relative_to(ROOT)}`" for path in MIRROR_STATUS_FILES if path.exists()),
        ]
    )
    OUT_REPORT.write_text("\n".join(report) + "\n", encoding="utf-8")


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--replace", action="store_true", help="Accepted for Makefile consistency; outputs are regenerated.")
    parser.parse_args()
    paths = mirrored_paths_by_candidate()
    rows = build_rows(paths) + build_batch006_rows(paths)
    OUT_TSV.parent.mkdir(parents=True, exist_ok=True)
    pd.DataFrame([{col: clean(row.get(col, "")) for col in COLUMNS} for row in rows]).to_csv(
        OUT_TSV, sep="\t", index=False
    )
    write_report(rows)


if __name__ == "__main__":
    main()
