def clean(value: Any) -> str:
    if value is None:
        return ""
    return str(value).replace("\t", " ").replace("\n", " ").replace("\r", " ").strip()


def mirrored_paths_by_candidate() -> dict[str, list[str]]:
    out: dict[str, list[str]] = {}
    for mirror_status in MIRROR_STATUS_FILES:
        if not mirror_status.exists():
            continue
        df = pd.read_csv(mirror_status, sep="\t", dtype=str).fillna("")
        if df.empty:
            continue
        for candidate_id, group in df[df["mirror_status"].eq("mirrored")].groupby("candidate_id"):
            paths = [path for path in group["local_path"].tolist() if path]
            out.setdefault(candidate_id, [])
            for path in paths:
                if path not in out[candidate_id]:
                    out[candidate_id].append(path)
    for raw_dir in RAW_DIRS:
        if not raw_dir.exists():
            continue
        for candidate_dir in sorted(path for path in raw_dir.iterdir() if path.is_dir()):
            paths = [
                str(path.relative_to(ROOT))
                for path in sorted(candidate_dir.rglob("*"))
                if path.is_file()
            ]
            if not paths:
                continue
            out.setdefault(candidate_dir.name, [])
            for path in paths:
                if path not in out[candidate_dir.name]:
                    out[candidate_dir.name].append(path)
    return out


def support(paths: dict[str, list[str]], candidate_id: str) -> str:
    return " | ".join(paths.get(candidate_id, []))


